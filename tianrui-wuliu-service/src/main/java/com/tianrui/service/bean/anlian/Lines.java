package com.tianrui.service.bean.anlian;

public class Lines {

	/** 货品名称*/
	private String hpmc;
	/** 数量*/
	private String sl;
	/** 单位*/
	private String dw;
	
	private String dwzl;
	/** 货品属性*/
	private String hpsx;
	public String getHpmc() {
		return hpmc;
	}
	public void setHpmc(String hpmc) {
		this.hpmc = hpmc;
	}
	public String getSl() {
		return sl;
	}
	public void setSl(String sl) {
		this.sl = sl;
	}
	public String getDw() {
		return dw;
	}
	public void setDw(String dw) {
		this.dw = dw;
	}
	public String getDwzl() {
		return dwzl;
	}
	public void setDwzl(String dwzl) {
		this.dwzl = dwzl;
	}
	public String getHpsx() {
		return hpsx;
	}
	public void setHpsx(String hpsx) {
		this.hpsx = hpsx;
	}
}
