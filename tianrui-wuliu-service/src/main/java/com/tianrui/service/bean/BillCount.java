package com.tianrui.service.bean;

public class BillCount {
    private String distinctCount;

    private String venderWeightCount;

    private String pickupWeightCount;

    private String unloadWeightCount;

    private String trueWeightCount;

	public String getDistinctCount() {
		return distinctCount;
	}

	public void setDistinctCount(String distinctCount) {
		this.distinctCount = distinctCount;
	}

	public String getVenderWeightCount() {
		return venderWeightCount;
	}

	public void setVenderWeightCount(String venderWeightCount) {
		this.venderWeightCount = venderWeightCount;
	}

	public String getPickupWeightCount() {
		return pickupWeightCount;
	}

	public void setPickupWeightCount(String pickupWeightCount) {
		this.pickupWeightCount = pickupWeightCount;
	}

	public String getUnloadWeightCount() {
		return unloadWeightCount;
	}

	public void setUnloadWeightCount(String unloadWeightCount) {
		this.unloadWeightCount = unloadWeightCount;
	}

	public String getTrueWeightCount() {
		return trueWeightCount;
	}

	public void setTrueWeightCount(String trueWeightCount) {
		this.trueWeightCount = trueWeightCount;
	}
    
    
}
