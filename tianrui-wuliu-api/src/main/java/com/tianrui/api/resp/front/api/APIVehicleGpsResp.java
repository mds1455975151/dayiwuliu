package com.tianrui.api.resp.front.api;

public class APIVehicleGpsResp {
	private String lat = "";
	private String lon = "";
	private String utc = "";

	public String getLat() {
		return lat;
	}

	public void setLat(String lat) {
		this.lat = lat;
	}

	public String getLon() {
		return lon;
	}

	public void setLon(String lon) {
		this.lon = lon;
	}

	public String getUtc() {
		return utc;
	}

	public void setUtc(String utc) {
		this.utc = utc;
	}

}